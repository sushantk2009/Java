import java.util.Scanner;
public class L_21_WhileLoop {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter The Number:");
        int num=sc.nextInt();
        int i=0;
        while(i<=num){
            System.out.println(i);
            i++;
}
    }
}
